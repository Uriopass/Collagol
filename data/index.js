function redrawCell(x, y, color) {
    if (x < 0 || y < 0 || x >= width || y >= height) {
        return
    }
    context.beginPath();
    context.rect(x * cellSize, y * cellSize, cellSize, cellSize);

    if (color === "") {
        let cell = grid[y][x];
        if (cell === 3) {
            color = erasecolor;
        } else if (cell === 2) {
            color = activatecolor;
        } else if (cell === 1) {
            color = fgcolor;
        } else {
            color = bgcolor;
        }
    }
    context.fillStyle = color;

    context.fill();
}

function getMousePos(canvas, evt) {
    let rect = canvas.getBoundingClientRect();
    return {
        x: (evt.clientX - rect.left),
        y: (evt.clientY - rect.top)
    };
}

function draw() {
    for (y = 0; y < height; y++) {
        for (x = 0; x < width; x++) {
            let cell = grid[y][x];
            if (cell === lastGrid[y][x]) {
                continue
            }
            context.beginPath();
            context.rect(x * cellSize, y * cellSize, cellSize, cellSize);
            if (x === lastpos.x && y === lastpos.y) {
                context.fillStyle = 'black';
            } else if (cell === 1) {
                context.fillStyle = fgcolor;
            } else if (cell === 2) {
                context.fillStyle = activatecolor;
            } else if (cell === 3) {
                context.fillStyle = erasecolor;
            } else {
                context.fillStyle = bgcolor;
            }
            context.fill();
        }
    }
}

function decode(string) {
    let cells = [];
    let ignore = false;
    let step = 1;
    let x = 0;
    let y = 0;
    let match, number;
    let width = 0,
        height = 0;
    let name = "";
    let naming = false;
    let firstletter = false;

    for (let i = 0; i < string.length; i++) {
        if (ignore) {
            if (string[i] === "\n") {
                ignore = false;
                naming = false;
            }
            if (naming) {
                name += string[i];
            }
            if (firstletter) {
                if (string[i] === "N") {
                    naming = true;
                }
                firstletter = false;
            }
            continue
        }
        switch (string[i]) {
            case "#":
            case "x":
            case "!":
                firstletter = true;
                ignore = true;
                continue;
            case "$":
                x = 0;
                y += step;
                step = 1;
                height = y > height ? y : height;
                continue;
            case "b":
                x += step;
                step = 1;
                continue;
            case "o":
                for (let j = 0; j < step; j++) {
                    cells.push([x++, y]);
                    width = x > width ? x : width;
                }
                step = 1;
                continue
        }
        match = string.slice(i).match(/[0-9]+/);
        if (match && !match.index) {
            number = match[0];
            step = parseInt(number);
            i += number.length - 1
        }
    }

    let grid = new Array(height + 1);
    for (let i = 0; i < grid.length; i++) {
        grid[i] = new Array(width);
        for (let j = 0; j < grid[i].length; j++) {
            grid[i][j] = 0;
        }
    }
    for (let i = 0; i < cells.length; i++) {
        let cell = cells[i];
        grid[cell[1]][cell[0]] = 1
    }
    return [grid, name.trim()]
}

