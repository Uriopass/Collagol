function decode(string) {
    let cells = [];
    let ignore = false;
    let step = 1;
    let x = 0;
    let y = 0;
    let match, number;
    let width = 0,
        height = 0;
    let name = "";
    let naming = false;
    let firstletter = false;

    for (let i = 0; i < string.length; i++) {
        if (ignore) {
            if (string[i] === "\n") {
                ignore = false;
                naming = false;
            }
            if (naming) {
                name += string[i];
            }
            if (firstletter) {
                if (string[i] === "N") {
                    naming = true;
                }
                firstletter = false;
            }
            continue
        }
        switch (string[i]) {
            case "#":
            case "x":
            case "!":
                firstletter = true;
                ignore = true;
                continue;
            case "$":
                x = 0;
                y += step;
                step = 1;
                height = y > height ? y : height;
                continue;
            case "b":
                x += step;
                step = 1;
                continue;
            case "o":
                for (let j = 0; j < step; j++) {
                    cells.push([x++, y]);
                    width = x > width ? x : width;
                }
                step = 1;
                continue
        }
        match = string.slice(i).match(/[0-9]+/);
        if (match && !match.index) {
            number = match[0];
            step = parseInt(number);
            i += number.length - 1
        }
    }

    let grid = new Array(height + 1);
    for (let i = 0; i < grid.length; i++) {
        grid[i] = new Array(width);
        for (let j = 0; j < grid[i].length; j++) {
            grid[i][j] = 0;
        }
    }
    for (let i = 0; i < cells.length; i++) {
        let cell = cells[i];
        grid[cell[1]][cell[0]] = 1
    }
    return [grid, name.trim()]
}

