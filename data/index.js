function receive(obj) {
    for (let i = 0; i < height; i++) {
        for (let j = 0; j < width; j++) {
            let val = obj.charAt(i * width + j);

            if (grid[i][j] < 2) {
                grid[i][j] = val === '1' ? 1 : 0;
            }
        }
    }

    draw();
}

function resetGrids() {
    for (let i = 0; i < height; i++) {
        for (let j = 0; j < width; j++) {
            if (grid[i][j] === 2) {
                grid[i][j] = 0;
                redrawCell(j, i, "");
            } else if (grid[i][j] === 3) {
                grid[i][j] = 0;
                redrawCell(j, i, "");
            }
        }
    }
}

function sendHandler() {
    let tosend = [];
    let todel = [];

    for (let i = 0; i < height; i++) {
        for (let j = 0; j < width; j++) {
            if (grid[i][j] === 2) {
                tosend.push([j, i]);
                grid[i][j] = 1;
            }
            if (grid[i][j] === 3) {
                todel.push([j, i]);
                grid[i][j] = 0;
            }
        }
    }
    ws.send(JSON.stringify([tosend, todel]));
    draw()
}

function mouseUpHandler() {
    if(document.getElementById("sendOnRelease").checked) {
        sendHandler();
        return
    }

    let tosend = [];
    let todel = [];

    for (let i = 0; i < height; i++) {
        for (let j = 0; j < width; j++) {
            if (grid[i][j] === 3) {
                todel.push([j, i]);
                grid[i][j] = 0;
                redrawCell(j, i, "")
            }
        }
    }

    if (todel.length === 0) {
        return;
    }

    ws.send(JSON.stringify([tosend, todel]));
}

function disableFor1Min() {
    document.getElementById("clearBoard").disabled = true;
    let counter = 60;
    document.getElementById("clearBoard").innerText = "Clear the board - " + counter + "s";
    let t = setInterval(function() {
        counter --;
        document.getElementById("clearBoard").innerText = "Clear the board - " + counter + "s";
        if(counter === 0) {
            clearTimeout(t);
            document.getElementById("clearBoard").disabled = false;
            document.getElementById("clearBoard").innerText = "Clear the board";
        }
    }, 1000);
}

function sendClear() {
    disableFor1Min();
    let tosend = [];
    let todel = [];

    for (let i = 0; i < height; i++) {
        for (let j = 0; j < width; j++) {
            todel.push([j, i]);
        }
    }
    ws.send(JSON.stringify([tosend, todel]));
}

