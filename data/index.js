function receive(obj) {
    for (let i = 0; i < height; i++) {
        for (let j = 0; j < width; j++) {
            lastGrid[i][j] = grid[i][j];
            let val = obj[i][j];
            if (grid[i][j] < 2) {
                grid[i][j] = val;
            }
        }
    }

    draw();
}

function resetGrids() {
    for (let i = 0; i < height; i++) {
        for (let j = 0; j < width; j++) {
            if (grid[i][j] === 2) {
                grid[i][j] = 0;
                redrawCell(j, i, "");
            } else if (grid[i][j] === 3) {
                grid[i][j] = 0;
                redrawCell(j, i, "");
            }
        }
    }
}

function sendHandler() {
    let tosend = [];
    let todel = [];

    for (let i = 0; i < height; i++) {
        for (let j = 0; j < width; j++) {
            if (grid[i][j] === 2) {
                tosend.push([j, i]);
                grid[i][j] = 1;
            }
            if (grid[i][j] === 3) {
                todel.push([j, i]);
                grid[i][j] = 0;
            }
        }
    }
    ws.send(JSON.stringify([tosend, todel]));
    draw()
}

function sendDeletion() {
    let tosend = [];
    let todel = [];

    for (let i = 0; i < height; i++) {
        for (let j = 0; j < width; j++) {
            if (grid[i][j] === 3) {
                todel.push([j, i]);
                grid[i][j] = 0;
                redrawCell(j, i, "")
            }
        }
    }

    if (todel.length === 0) {
        return;
    }

    ws.send(JSON.stringify([tosend, todel]));
}

