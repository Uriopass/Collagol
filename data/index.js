function init(config) {
    height = config.height;
    width = config.width;
    cellSize = config.cellSize;

    canvas.width = width * cellSize;
    canvas.height = height * cellSize;
    canvas.style.width = width * cellSize + "px";
    canvas.style.height = height * cellSize + "px";
    document.getElementById("game").style.width = (width * cellSize + 300) + "px";

    console.log(config);

    grid = new Array(height);
    lastGrid = new Array(height);
    for (let i = 0; i < height; i++) {
        grid[i] = new Array(width);
        lastGrid[i] = new Array(width);
        for (let j = 0; j < height; j++) {
            grid[i][j] = 0;
            lastGrid[i][j] = -5;
        }
    }
    selectPattern(-1);
    draw();
    initOk = true;
}

function receive(obj) {
    for (let i = 0; i < height; i++) {
        for (let j = 0; j < width; j++) {
            lastGrid[i][j] = grid[i][j];
            let val = obj[i][j];
            if (grid[i][j] < 2) {
                grid[i][j] = val;
            }
        }
    }

    draw();
}

function resetGrids() {
    for (let i = 0; i < height; i++) {
        for (let j = 0; j < width; j++) {
            if (grid[i][j] === 2) {
                grid[i][j] = 0;
                redrawCell(j, i);
            } else if (grid[i][j] === 3) {
                grid[i][j] = 0;
                redrawCell(j, i);
            }
        }
    }
}

function sendHandler() {
    let tosend = [];
    let todel = [];

    for (let i = 0; i < height; i++) {
        for (let j = 0; j < width; j++) {
            if (grid[i][j] === 2) {
                tosend.push({
                    x: j,
                    y: i
                });
                grid[i][j] = 1;
            }
            if (grid[i][j] === 3) {
                todel.push({
                    x: j,
                    y: i
                });
                grid[i][j] = 0;
            }
        }
    }
    ws.send(JSON.stringify([tosend, todel]));
    draw()
}

function sendDeletion() {
    let tosend = [];
    let todel = [];

    for (let i = 0; i < height; i++) {
        for (let j = 0; j < width; j++) {
            if (grid[i][j] === 3) {
                todel.push({
                    x: j,
                    y: i
                });
                grid[i][j] = 0;
                redrawCell(j, i, "")
            }
        }
    }

    if (todel.length === 0) {
        return;
    }

    ws.send(JSON.stringify([tosend, todel]));
}

function sendClear() {
    let tosend = [];
    let todel = [];

    for (let i = 0; i < height; i++) {
        for (let j = 0; j < width; j++) {
            todel.push({
                x: j,
                y: i
            });
            grid[i][j] = 0;
        }
    }
    ws.send(JSON.stringify([tosend, todel]));
    draw()
}

