let ws;
let messageWs;

function initws() {
    ws = new WebSocket("ws://" + document.location.host + "/echo");
    ws.onopen = function (evt) {
        console.log("OPEN");
    };
    ws.onclose = function (evt) {
        console.log("CLOSE retrying...");
        setTimeout(function () {
            initws()
        }, 1000);
    };
    ws.onmessage = function (evt) {
        if (!initOk)
            return;
        let reader = new FileReader();
        reader.readAsArrayBuffer(evt.data);
        reader.addEventListener("loadend", function(e)
        {
            let buffer = new Uint8Array(e.target.result);
            let decompressed = LZMA.decompress(buffer);
            receive(JSON.parse(decompressed));
        });
    };
    ws.onerror = function (evt) {
        console.log("ERROR: " + evt.data);
    };
}

function initChatroom() {
    let messageDiv = document.getElementById("messages");
    messageWs = new WebSocket("ws://" + document.location.host + "/message");
    messageWs.onopen = function (evt) {
        console.log("OPEN");
        messageDiv.innerHTML = "";
    };
    messageWs.onclose = function (evt) {
        console.log("Lost connection to chat retrying...");
        setTimeout(function () {
            initChatroom()
        }, 1000);
    };
    messageWs.onmessage = function (evt) {
        messageDiv.innerHTML += `<li class="message">${evt.data}</li>`;
        messageDiv.scrollTop = messageDiv.scrollHeight;
    };
    messageWs.onerror = function (evt) {
        console.log("ERROR: " + evt.data);
    };
    document.getElementById("usernameText").value = Cookies.get("username", " ");
}

function initRLEs() {
    let rles = loadRLEs();
    for(let i = 0 ; i < rles.length ; i++) {
        addRLE(rles[i])
    }
}

function initConfig(config) {
    height = config.height;
    width = config.width;
    cellSize = config.cellSize;

    canvas.width = width * cellSize;
    canvas.height = height * cellSize;
    canvas.style.width = width * cellSize + "px";
    canvas.style.height = height * cellSize + "px";
    document.getElementById("game").style.width = (width * cellSize + 600) + "px";

    console.log(config);

    grid = new Array(height);
    lastGrid = new Array(height);
    for (let i = 0; i < height; i++) {
        grid[i] = new Array(width);
        lastGrid[i] = new Array(width);
        for (let j = 0; j < height; j++) {
            grid[i][j] = 0;
            lastGrid[i][j] = -5;
        }
    }
    selectPattern(0);
    draw();
    initOk = true;
}

